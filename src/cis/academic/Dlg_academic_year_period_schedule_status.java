    private void ok() {
        int status = 0;
        if (jCheckBox1.isSelected()) {
            status = 1;
        }
        if (jCheckBox2.isSelected()) {
            status = 2;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(status));
        }
    }
