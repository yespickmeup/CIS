    private void ok() {
        int max_students = FitIn.toInt(tf_field14.getText());
        int no_of_sections = FitIn.toInt(tf_field15.getText());
        String section_name=tf_field16.getText();
        if (max_students < 0) {
            Alert.set(0, "Input max students");
            tf_field14.grabFocus();
            return;
        }
        if (no_of_sections < 0) {
            Alert.set(0, "Input no of sections");
            tf_field15.grabFocus();
            return;
        }
        if(section_name.isEmpty() && no_of_sections==1){
            Alert.set(0, "Input section name");
            tf_field16.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(max_students, no_of_sections,section_name));
        }
    }
