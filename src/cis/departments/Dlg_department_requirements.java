    private void ok() {
        String requirement = jTextArea1.getText();
        if (requirement.isEmpty()) {
            Alert.set(0, "Input text!");
            jTextArea1.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(requirement));
        }
    }
