    private void ok() {
        double amount = FitIn.toDouble(tf_field15.getText());
        if (amount < 0) {
            Alert.set(0, "Enter amount");
            tf_field15.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(amount));
        }
    }
