    School_settings.to_school_settings setting = null;

    private void ret_settings() {
        List<School_settings.to_school_settings> settings = School_settings.ret_data(" where name like 'Subject Loading overload' ");
        if (!settings.isEmpty()) {
            School_settings.to_school_settings to = (School_settings.to_school_settings) settings.get(0);
            setting = to;
            tf_screen_name.setText("" + to.amount);
            tf_screen_name1.setText("" + to.amount2);
        }
    }

    private void save() {
        double amount1 = FitIn.toDouble(tf_screen_name.getText());
        double amount2 = FitIn.toDouble(tf_screen_name1.getText());

        if (setting != null) {
            School_settings.to_school_settings new_setting = new School_settings.to_school_settings(setting.id, "Subject Loading overload", "Units", amount1, amount2);

            Window p = (Window) this;
            Dlg_confirm_action nd = Dlg_confirm_action.create(p, true);
            nd.setTitle("");
            nd.setCallback(new Dlg_confirm_action.Callback() {

                @Override
                public void ok(CloseDialog closeDialog, Dlg_confirm_action.OutputData data) {
                    closeDialog.ok();
                    School_settings.update_data(new_setting);
                    ret_settings();
                    Alert.set(2, "");
                }
            });
            nd.setLocationRelativeTo(this);
            nd.setVisible(true);
        }

    }
